    private void tempoAcabou(){
        
        tempoAcabado = new Timer(500, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
               if(timerCronometro.getForeground() == Color.RED)
               {
                   timerCronometro.setForeground(Color.BLACK);
               } else {
                   timerCronometro.setForeground(Color.RED);
               }
            }
        });
        tempoAcabado.start();
    }
    
