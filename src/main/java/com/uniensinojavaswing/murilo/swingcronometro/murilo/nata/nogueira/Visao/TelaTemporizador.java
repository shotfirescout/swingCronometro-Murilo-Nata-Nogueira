    private void btnPararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPararActionPerformed
        timerCronometro.setText("00:00:00");
        controle.zerarTimer();
        timerCronometro.setForeground(Color.black);
        if(timer != null)
        { 
            timer.stop();
        }
        if(tempoAcabado != null)
        {
            tempoAcabado.stop();
        }
        
    }//GEN-LAST:event_btnPararActionPerformed

    private void btnRedefenirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRedefenirActionPerformed
        try{
             int segundos = 0;
             int minutos = 0;
             int horas = 0;
            if(!textFieldSegundos.getText().equals(""))
            {
                segundos = Integer.parseInt(textFieldSegundos.getText());
            }
            
            if(!textFieldMinutos.getText().equals(""))
            {
                 minutos = Integer.parseInt(textFieldMinutos.getText());
            }
            
            if(!textFieldHoras.getText().equals("")){
                horas = Integer.parseInt(textFieldHoras.getText());
            }
            //Verifica duas condições, primeiro se segundos ou minutos são maiores que 59
            if(segundos > 59 || minutos > 59){
                JOptionPane.showMessageDialog(this, "Segundos|Minutos não podem ultrapassar 59");
            } else if (segundos < 0  || minutos < 0 || horas < 0){ //Segunda condição, verifica se os valores são negativos
                JOptionPane.showMessageDialog(this, "os valores não podem ser negativos");
            } else { //caso ambos sejam falsos, é possível atribuir os valores
                controle.redefenirTempo(segundos,minutos,horas);
                timerCronometro.setText(controle.retornaTempo());
            }
        } catch(NumberFormatException format){
            JOptionPane.showMessageDialog(this, "Os valores inseridos não são validos, coloque só mente valores inteiros");
        }
        
    }//GEN-LAST:event_btnRedefenirActionPerformed

